        public static bool TryLoadProductKey(out string key, out string source)
        {
            key = null;
            source = null;

            if (TryLoadProductKeyFromVrom(out key, out source))
            {
                return true;
            }

            return TryLoadProductKeyFromKeyFile(out key, out source);
        }

        public static bool TryLoadProductKeyFromVrom(out string key, out string source)
        {
            key = null;
            source = null;
            var appData = Environment.GetFolderPath(Environment.SpecialFolder.ApplicationData);
            if (string.IsNullOrWhiteSpace(appData))
            {
                return false;
            }

            var dir = Path.Combine(appData, "non-standard.com");
            if (!Directory.Exists(dir))
            {
                return false;
            }

            foreach (var path in Directory.GetFiles(dir, "VROM_*.bin"))
            {
                byte[] data;
                try
                {
                    data = File.ReadAllBytes(path);
                }
                catch (IOException)
                {
                    continue;
                }
                catch (UnauthorizedAccessException)
                {
                    continue;
                }

                if (TryExtractKeyFromEeprom(data, out key))
                {
                    source = path;
                    return true;
                }
            }

            return false;
        }

        public static bool TryLoadProductKeyFromKeyFile(out string key, out string source)
        {
            key = null;
            source = null;

            foreach (var dir in new[] { AppDomain.CurrentDomain.BaseDirectory, Environment.CurrentDirectory })
            {
                if (string.IsNullOrWhiteSpace(dir))
                {
                    continue;
                }

                var keyPath = Path.Combine(dir, "llspa3_key.txt");
                if (!File.Exists(keyPath))
                {
                    continue;
                }

                foreach (var encoding in MemoEncodings())
                {
                    string text;
                    try
                    {
                        text = File.ReadAllText(keyPath, encoding);
                    }
                    catch (IOException)
                    {
                        continue;
                    }
                    catch (UnauthorizedAccessException)
                    {
                        continue;
                    }

                    if (TryFindKeyInText(text, out key))
                    {
                        source = keyPath;
                        return true;
                    }
                }
            }

            return false;
        }

        public static bool TryBuildNewSetupKeyBuffer(bool isRgb888, bool is3d, string key, out byte[] setupKeyBuffer)
        {
            var baseBuffer = SelectNewSetupKeyBuffer(isRgb888, is3d);
            setupKeyBuffer = new byte[baseBuffer.Length];
            Buffer.BlockCopy(baseBuffer, 0, setupKeyBuffer, 0, baseBuffer.Length);
            if (string.IsNullOrWhiteSpace(key))
            {
                return false;
            }

            var keyData = new byte[KeyBytes];
            if (!KeyToData(key, keyData, true) && !KeyToData(key, keyData, false))
            {
                return false;
            }

            setupKeyBuffer[11] = keyData[11];
            setupKeyBuffer[13] = keyData[10];
            return true;
        }

        // cc3dsfs Optimize Old 3DS (is_new_device=false, is_old_firmware=false).
        // Returns the base setup buffer; if a valid key is supplied it is decoded
        // with the old serialization and folded in with the nibble-shift placement
        // from populate_capture_setup_key_buffer. cc3dsfs never fails capture on a
        // missing/invalid key — it just skips the fold — so this always succeeds.
        public static byte[] BuildOldSetupKeyBuffer(bool isRgb888, bool is3d, string key)
        {
            var baseBuffer = SelectOldSetupKeyBuffer(isRgb888, is3d);
            var setupKeyBuffer = new byte[baseBuffer.Length];
            Buffer.BlockCopy(baseBuffer, 0, setupKeyBuffer, 0, baseBuffer.Length);
            if (string.IsNullOrWhiteSpace(key))
            {
                return setupKeyBuffer;
            }

            var keyData = new byte[KeyBytes];
            if (!KeyToData(key, keyData, false))
            {
                return setupKeyBuffer;
            }

            setupKeyBuffer[11] |= (byte)((keyData[11] & 0xF) << 4);
            setupKeyBuffer[13] = (byte)(((keyData[10] & 0xF) << 4) | (keyData[11] >> 4));
            setupKeyBuffer[15] = (byte)(keyData[10] >> 4);
            return setupKeyBuffer;
        }

        public static bool TryValidateProductKey(string key)
        {
            var keyData = new byte[KeyBytes];
            return KeyToData(key, keyData, true) || KeyToData(key, keyData, false);
        }

        // cc3dsfs get_device_id_from_key: read_be64 of the decoded key bytes.
        // Used only to sanity-check our live device-id read against the key; the
        // value is compared, never logged.
        public static bool TryGetDeviceIdFromKey(string key, bool isNewCc, out ulong deviceId)
        {
            deviceId = 0;
            var keyData = new byte[KeyBytes];
            if (!KeyToData(key, keyData, isNewCc))
            {
                return false;
            }

            ulong value = 0;
            for (var i = 0; i < 8; i++)
            {
                value = (value << 8) | keyData[i];
            }

            deviceId = value;
            return true;
        }

