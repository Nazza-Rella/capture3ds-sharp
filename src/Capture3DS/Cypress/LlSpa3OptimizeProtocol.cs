            if (eepromData == null || eepromData.Length < EepromNewSize)
            {
                return false;
            }

            var crc = CalcCrc32Adler(eepromData, EepromStructSize);
            var stored = ReadLe32(eepromData, EepromCrcPos);
            if (crc != stored)
            {
                return false;
            }

            // cc3dsfs Optimize uses key ID 0x66. The LL-SPA3 n3DS_view VROM
            // cache observed here uses 0xD3 with the same key placement.
            if (eepromData[8] != 0x66 && eepromData[8] != 0xD3)
            {
                return false;
            }

            key = ReadString(eepromData, 0x10, KeyCharsWithDashes);
            return TryValidateProductKey(key);
        }
        private static Encoding[] MemoEncodings()
        {
            try
            {
                return new[] { Encoding.UTF8, Encoding.GetEncoding(WindowsJapaneseCodePage), Encoding.Default };
            }
            catch (ArgumentException)
            {
                return new[] { Encoding.UTF8, Encoding.Default };
            }
        }

        private static bool TryFindKeyInText(string text, out string key)
        {
            key = null;
            if (string.IsNullOrEmpty(text))
            {
                return false;
            }

            for (var start = 0; start < text.Length; start++)
            {
                var sb = new StringBuilder(KeyChars);
                for (var pos = start; pos < text.Length && sb.Length <= KeyChars; pos++)
                {
                    var ch = text[pos];
                    if (char.IsLetterOrDigit(ch))
                    {
                        sb.Append(ch);
                        if (sb.Length == KeyChars && TryTokenAsKey(sb.ToString(), out key))
                        {
                            return true;
                        }

                        continue;
                    }

                    if (IsKeySeparator(ch))
                    {
                        continue;
                    }

                    break;
                }
            }

            return false;
        }


        private static bool TryTokenAsKey(string token, out string key)
        {
            key = null;
            if (string.IsNullOrWhiteSpace(token))
            {
                return false;
            }

            var cleaned = CleanKey(token);
            if (cleaned.Length != KeyChars)
            {
                return false;
            }

            if (!TryValidateProductKey(cleaned))
            {
                return false;
            }

            key = cleaned;
            return true;
        }

        private static bool KeyToData(string input, byte[] output, bool isNewCc)
        {
            var cleaned = CleanKey(input);
            if (cleaned.Length != KeyChars)
            {
                return false;
            }

            var keyStringBytes = Encoding.ASCII.GetBytes(cleaned);
            var packedKeyBytes = new byte[KeyBytes + 1];
            if (!KeyToKeyBytes(keyStringBytes, packedKeyBytes))
            {
                return false;
            }

            return KeyBytesToSerialBytes(packedKeyBytes, output, isNewCc);
        }

        private static string CleanKey(string input)
        {
            var sb = new StringBuilder(input.Length);
            foreach (var ch in input)
            {
                if (ch == '-' || ch == '&' || ch == ':')
                {
                    continue;
                }

                sb.Append(char.ToUpperInvariant(ch));
            }

            return sb.ToString();
        }

        private static bool KeyToKeyBytes(byte[] input, byte[] output)
        {
            ushort valueConverted = 0;
            var convertedPos = 0;
            var outIndex = 0;
            for (var i = 0; i < KeyChars; i++)
            {
                var character = input[i];
                if (OptimizeSerialKeyToByteTable[character] == 0xFF)
                {
                    return false;
                }

                valueConverted |= (ushort)(OptimizeSerialKeyToByteTable[character] << convertedPos);
                convertedPos += 5;
                if (convertedPos >= 8)
                {
                    output[outIndex++] = (byte)(valueConverted & 0xFF);
                    convertedPos -= 8;
                    valueConverted >>= 8;
                }
            }

            if (convertedPos > 0)
            {
                output[outIndex] = (byte)(valueConverted & 0xFF);
            }

            return true;
        }

        private static bool KeyBytesToSerialBytes(byte[] input, byte[] output, bool isNewCc)
        {
            var xorFactor = isNewCc ? 0x5A : 0xAB;
            var tableAccessingBaseValue = isNewCc ? -0x45 : 0x22;
            for (var i = 0; i < KeyBytes; i++)
            {
                output[KeyBytesToSerialBytesMap[i]] = input[i];
            }

            var checksumByte = (output[0] ^ xorFactor) & 0xFE;
            output[0] = (byte)(output[0] & 1);
            for (var i = 1; i < 8; i++)
            {
                var tableIndex = (checksumByte + (i * tableAccessingBaseValue)) & 0xFF;
                var xorValue = ReadLe32(AdlerCrc32TableSp, tableIndex * 4);
                WriteLe32(output, i, ReadLe32(output, i) ^ xorValue);
            }

            byte sum = 0;
            for (var i = 0; i < KeyBytes - 1; i++)
            {
                sum += output[i];
            }

            return (ReadLe32(AdlerCrc32TableSp, sum * 4) & 0xFE) == (uint)checksumByte;
        }

        private static uint CalcCrc32Adler(byte[] data, int size)
        {
            uint value = 0xFFFFFFFF;
            for (var i = 0; i < size; i++)
            {
                var innerValue = (byte)(data[i] ^ value);
                value >>= 8;
                value ^= ReadLe32(AdlerCrc32TableSp, innerValue * 4);
            }

            return ~value;
        }

        private static uint ReadLe32(byte[] data, int offset)
        {
            return (uint)(data[offset] | (data[offset + 1] << 8) | (data[offset + 2] << 16) | (data[offset + 3] << 24));
        }

        private static void WriteLe32(byte[] data, int offset, uint value)
        {
            data[offset] = (byte)(value & 0xFF);
            data[offset + 1] = (byte)((value >> 8) & 0xFF);
            data[offset + 2] = (byte)((value >> 16) & 0xFF);
            data[offset + 3] = (byte)((value >> 24) & 0xFF);
        }

        private static string ReadString(byte[] data, int offset, int size)
        {
            var count = 0;
            while (count < size && data[offset + count] != 0)
            {
                count++;
            }

            return Encoding.ASCII.GetString(data, offset, count);
        }

        private static bool IsKeySeparator(char ch)
        {
            return ch == '-' || ch == '&' || ch == ':' || ch == '：' || char.IsWhiteSpace(ch);
        }
